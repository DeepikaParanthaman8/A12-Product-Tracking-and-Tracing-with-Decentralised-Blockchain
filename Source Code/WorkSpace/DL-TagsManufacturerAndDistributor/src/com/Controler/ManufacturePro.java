package com.Controler;

public class ManufacturePro {

	private String name, proid, price, image, proname, desci, packect,
			packdate;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getProid() {
		return proid;
	}

	public void setProid(String proid) {
		this.proid = proid;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getImage() {
		return image;
	}

	public void setImage(String image) {
		this.image = image;
	}

	public String getProname() {
		return proname;
	}

	public void setProname(String proname) {
		this.proname = proname;
	}

	public String getDesci() {
		return desci;
	}

	public void setDesci(String desci) {
		this.desci = desci;
	}

	public void setPackect(String packect) {
		this.packect = packect;
	}

	public String getPackect() {
		return packect;
	}

	public void setPackdate(String packdate) {
		this.packdate = packdate;
	}

	public String getPackdate() {
		return packdate;
	}

}
